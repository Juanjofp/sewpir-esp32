var net = require('net');
var buffer = new Buffer([0x53, 0x45, 0x57, 0x01, 0x17, 0x00, 0x02, 0x04, 0x0a, 0x0f, 0xae, 0x0e, 0x04, 0x01, 0x01, 0x00, 0xcd, 0xcc, 0x8c, 0x40, 0x57, 0x45, 0x53]);
var server = net.createServer(function(socket) {
	socket.write(buffer);
	socket.on('data', function(data){
        console.log('Buffer', data);
        socket.write(data);
	});
});
server.listen(6966, '127.0.0.1');